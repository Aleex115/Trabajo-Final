    public void AgregarMec() {
        try {
            Connection conexion = Conex.devolverConex();
            PreparedStatement sentencia = (PreparedStatement) conexion.prepareStatement("select dni, nombre from mecanicos");
            ResultSet rs = sentencia.executeQuery();
            while (rs.next()) {
                String nombre = rs.getString("nombre");
                modelMec.addElement(nombre);
                dniMec.add(rs.getString("dni"));
            }
            rs.close();
            sentencia.close();
            Conex.CerrarConex();
        } catch (CommunicationsException e) {
            JOptionPane.showMessageDialog(this, "La base de datos no esta encendida");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            ;
        }
    }

    public void AgregarCoches() {
        try {
            Connection conexion = Conex.devolverConex();
            PreparedStatement sentencia = (PreparedStatement) conexion.prepareStatement("select n_bastidor, marca,modelo from coches");
            ResultSet rs = sentencia.executeQuery();
            while (rs.next()) {
                String modelo = rs.getString("modelo");
                String marca = rs.getString("marca");

                modelCoch.addElement(marca + " " + modelo);
                nBas.add(rs.getString("n_bastidor"));
            }
            rs.close();
            sentencia.close();
            Conex.CerrarConex();
        } catch (CommunicationsException e) {
            JOptionPane.showMessageDialog(this, "La base de datos no esta encendida");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            ;
        }
    }

