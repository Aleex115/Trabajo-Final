    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        if (Comprobaciones()) {
            try {
                Connection conexion = Conex.devolverConex();
                String sql = "insert into reparaciones values (?, ?, ?, ?, ?,? )";
                PreparedStatement sentencia = (PreparedStatement) conexion.prepareStatement(sql);
                sentencia.setString(1, dniMec.get(cmbMec.getSelectedIndex()));
                sentencia.setString(2, nBas.get(cmbCoch.getSelectedIndex()));
                sentencia.setInt(3, Integer.parseInt(txtBas.getText()));
                sentencia.setString(4, fechaIni.getText());
                sentencia.setString(5, jTextArea2.getText());
                sentencia.setString(6,fechaFin.getText());
                sentencia.executeUpdate();
                JOptionPane.showMessageDialog(this, "Se ha realizado la inserción");
                sentencia.close();
                Conex.CerrarConex();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

