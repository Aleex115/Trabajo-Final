    public boolean Comprobaciones() {
        if (fechaIni.getText().compareTo(fechaFin.getText()) == 1) {
            JOptionPane.showMessageDialog(this, "La fecha de inicio es mayor a la de inicio");
            return false;
        }
        try {
            Connection conexion = Conex.devolverConex();
            PreparedStatement sentencia = (PreparedStatement) conexion.prepareStatement("select n_reparacion from reparaciones");
            ResultSet rs = sentencia.executeQuery();
            while (rs.next()) {
                String n = rs.getString("n_reparacion");
                if ((n.equals(txtBas.getText())) || (txtBas.getText().isEmpty())) {
                    JOptionPane.showMessageDialog(this, "El numero de reparacion ya esta usado o esta vacio");
                    return false;
                }
            }
            rs.close();
            sentencia.close();
            Conex.CerrarConex();
        } catch (CommunicationsException e) {
            JOptionPane.showMessageDialog(this, "La base de datos no esta encendida");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            ;
        }
        if (jTextArea2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La descripcion no puede estar vacia");
            return false;
        }
        return true;
    }

