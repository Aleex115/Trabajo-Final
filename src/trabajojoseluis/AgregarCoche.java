    public String getExtension(File file) {
        String name = file.getName();
        int lastIndex = name.lastIndexOf('.');
        if (lastIndex > 0 && lastIndex < name.length() - 1) {
            return name.substring(lastIndex + 1);
        } else {
            return "";
        }
    }

    public void AgregarSentencia(PreparedStatement sentecia) {
        try {
            sentecia.setString(1, n_bastidor.getText());
            sentecia.setInt(2, Integer.parseInt(precio.getText()));
            sentecia.setString(3, (String) estado.getSelectedItem());
            sentecia.setInt(4, Integer.parseInt(km.getText()));
            sentecia.setInt(5, Integer.parseInt(cv.getText()));
            sentecia.setString(6, (String) categoria.getSelectedItem());
            sentecia.setString(7, marca.getText());
            sentecia.setString(8, modelo.getText());
            sentecia.setString(9, dni.get(dni_proveedor.getSelectedIndex()));
            sentecia.setString(10, fecha);
            sentecia.setString(11, origen.getName());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }

